package co.gov.movilidadbogota.sipa.serv.api;

/**
 * Cotenedor de un valor de variable de proceso
 */
public class ProcessVariableValue {

    private String value;
    private String type;

    public ProcessVariableValue(String value) {
        this.value = value;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
